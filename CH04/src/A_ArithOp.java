
public class A_ArithOp {

	public static void main(String[] args) {
		int num1 =7;
		int num2 =3;
		
		System.out.println("num1 + num2 = " + (num1 + num2));
		// ("num1 + num2 =" 는 num1 +num2 로 출력됨 그래서
		// +(num1 +num2)); 로 마무리를 해줘야됨
		
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));
		// /는 몫을 구함 7나누기 3 = 2
		System.out.println("num1 % num2 = " + (num1 % num2));
		// %는 나머지 구함 7나누기 3 = 1
	
				
	}

}
